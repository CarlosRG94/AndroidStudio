package com.example.jsonprofesor;

public class Member {
    private String name;
    private long age;
    private String secretIdentity;
    private String[] powers;


    public String getName() { return name; }

    public void setName(String value) { this.name = value; }


    public long getAge() { return age; }

    public void setAge(long value) { this.age = value; }


    public String getSecretIdentity() { return secretIdentity; }

    public void setSecretIdentity(String value) { this.secretIdentity = value; }


    public String[] getPowers() { return powers; }

    public void setPowers(String[] value) { this.powers = value; }

}
